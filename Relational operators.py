#Relational Operators: Relational operators compares the values. 
# It either returns True or False according to the condition.

a = 15
b = 32
# a > b is False 
print(a > b)
# a < b is True 
print(a < b)
# a == b is False 
print(a == b)
# a != b is True 
print(a != b)
# a >= b is False 
print(a >= b)
# a <= b is True 
print(a <= b)

